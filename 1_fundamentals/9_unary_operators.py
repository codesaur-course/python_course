# OPERADORES UNÁRIOS

"""
Diferente de outras linguagens Python não possui ( ++ ) como operador de incremento, como no exemplo abaixo:
"""

a = 3
# a++
print(a)

"""
Usar ( ++ ) trará um erro de sintaxe. Em Python o equivalente de incremento seria ( += ), como no exemplo abaixo:
"""

a += 1
print(a)

"""
O mesmo acontece com os demais operadores aritméticos.
"""

a -= 1
print(a)
