# OPERADORES RELACIONAIS

"""
Operadores relacionais visam comparar valores estabelecendo se eles sÃ£o verdadeiros ou falsos
"""

# OPERADOR RELACIONAL - MAIOR QUE: ( > )
print(3 > 2)

# OPERAODR RELACIONAL - MAIOR OU IGUAL QUE: ( >= )
print(3 >= 0)

# OPERADOR RELACIONAL - MENOR QUE: ( < )
print(5 < 4)

# OPERADOR RELACIONAL - MENOR OU IGUAL QUE: ( <= )
print(5 <= 7)

# OPERADOR REALCIONAL - IGUAL A: ( == )
print(5 == 5)
print(5 == '5')

# OPERADOR RELACIONAL - DIFERENTE QUE: ( != )
print(8 != 9)
print(9 != '9')
print(8 != 8)
