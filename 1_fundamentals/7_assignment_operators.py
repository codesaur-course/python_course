# OPERADORES DE ATRIBUÍÇÃO

"""
Como o próprio nome diz Operadores de Atribuíção visam atribuir alguma ação a um valor, como veremos nos exemplos
abaixo:
"""

# OPERADOR DE ATRIBUÍCÃO ( = )
a = 10
print(a)

"""
A partir  daqui os operadores atribuem, subtraem valores.
"""

# OPERADOR DE ATRIBUÍÇÃO ADITIVA ( += )
a += 15 # neste caso funciona como se eu estivesse fazendo a seguinte operação a = a + 15
print(a)

# OPERADOR DE ATRIBUÍÇÃO SUBTRATIVA ( -= )
a -= 2
print(a)

# OPERADOR DE ATRIBUÍÇÃO MULTIPLICATIVA ( *= )
a *= 2
print(a)

# OPERADOR DE ATRIBUÍÇÃO EXPONENCIAL ( **= )
a **= 5
print(a)

# OPERADOR DE ATRIBUÍÇÃO DIVISIVA ( /= )
a /= 2
print(a)

# OPERADOR DE ATRIBUÍÇÃO DE RESTO DE DIVISÃO ( %= )
a %= 3
print(a)

# OPERADOR DE ATRIBUÍÇÃO DE DIVISÃO INTEIRA ( //= )
a //= 2
print(a)
