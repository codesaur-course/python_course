# OPERADORES ARITMÉTICOS

"""
Operadores aritméticos são nada mais nada menos do que os sinais de operação matemática. Veremos cada um deles
abaixo.
"""

# OPERADOR DE SOMA
print(2 + 3)

# OPERADOR DE SUBTRAÇÃO
print(9 - 3)

# OPERADOR DE MULTIPLICAÇÃO
print(9 * 3)

# OPERADOR DE DIVISÃO
print(8.5 / 2)

# OPERADOR DE DIVISÃO COM RESULTADOS INTEIROS
print(8.5 // 2)

# OPERADOR DE EXPONENCIAÇÃO OU POTÊNCIA
print(5 ** 2)

# OPERADOR DE RESTO DE DIVISÃO
print(5 % 2)
