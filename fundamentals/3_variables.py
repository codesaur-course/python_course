# VARIÁVEIS

"""
Variáveis não são nada mais do que espaços de memória onde iremos armazenar algum dado específico, neste caso
nomeamos a varíavel, o nome neste caso, vale ressaltar, é o identificador da variável que virá seguida do sinal de
igual ( = ), porém esse sinal em programação Python não significa IGUAL mas sim que RECEBE, ou seja, quando escrevemos
a seguinte variável:

a = valor

Lemos -> Identificador A, RECEBE (=), valor
"""

a = 10
b = 22.5

print(a + b)

"""
Lembrando que podemos alterar o valor da variável como no exemplo abaixo, assim ele irá apresentar todos os valores,
porém a partir deste ponto a variável A recebe um outro valor enquanto que B mantém seu valor original.
"""

a = "Agora este é o valor de A"

print(a)
print(b)
